<h2>Registering and Paying</h2>

<p>First you sign in to the site by entering your email, and the site emails you a link. We don't make you pick a password because who needs more passwords?</p>

<p>You can also log in via Google with a gmail address. We thought about letting you do this via Facebook, but screw Facebook.</p>

<p>After you sign up, you click on a class and then can register for it. If it's full you will be put on a waiting list.</p>

<p>A lot happens over email. That's how you will be told if a spot opens up in a class you were on a waiting list for. That's how you'll get your Zoom link to take the class. If anything changes with regard to the class, you'll get an email.</p>

<p>After you have been enrolled, you pay on your own by sending money via Venmo or Paypal (account info sent via email after you enroll). We ask that you pay by the start of class.</p>


<h2>Using Zoom</h2>

<p>All classes happen via Zoom.</p>

<p>After you are enrolled, you'll get an email with a link to a Zoom session.</p>

<p>Headphones can help if you are in a noisy area, or have an old computer with a weaker microphone. But they're not required. Weirdly, those bluetooth Apple earbuds aren't great. People sound like they're talking from inside of a pipe. Better to use nothing if that's all you got.</p>


<h2>Refund Policy</h2>

<p>We want to have a generous refund policy. With more than <b>12 hours notice</b>, you get a full refund. Less than that, we may ask you to pay if the class had been sold out and we could have sold your spot.</p>

<p>If you do drop out with less than 12 hours notice, but someone takes your spot -- we will refund your money.</p>

<h2>Prior Experience?</h2>

<p>Some classes require you to have taken other classes. But if you feel you have a lot of improv experience that means you're ready for upper levels email Will (address at bottom of site) and he might grant an exception.</p>

<p>If you have very little improv experience, that's fine! But let your teacher know when you start. They'll make sure to keep you up to speed as it goes. Improv is fun and easy to get going with.</p>


<h2>Mailing List</h2>

<p>Our mailing list is the best way to be kept up on new classes.</p>

<p>You are NOT automatically put on a mailing list when you take a class. You have to explicitly <a href="http://eepurl.com/R2Ytz">sign yourself up</a>.</p>


<h2>Will Hines Multi-Week Classes</h2>
<p>Each one of these is five two-hour sessions, plus a show. 8 students per class, $220 each.</p>

<p>Next batch of these will go up September 2020. It'll be a lottery to get in. Join the <a href="http://eepurl.com/R2Ytz">mailing list</a> to hear about them when they are offered.</p>


<dl class="row">
  <dt class="col-sm-3">Better Games Level One</dt>
  <dd class="col-sm-9">We work towards doing a monolgue show. Someone tells a true story and we pull scenes from it. Subject covered: finding the fool, saying why, supporting games through walk-ons and tag-outs, looking for contrast, self-destruction and absurdity. Some improv experience preferred.</dd>

  <dt class="col-sm-3">Better Games Level Two</dt>
  <dd class="col-sm-9">
    We work towards doing a version of the Harold, where you revisit scenes a few times to make the scenes feel like an interwoven play. Must have taken Better Games Level One or have an exception based on outside experience.
  </dd>

  <dt class="col-sm-3">Better Games Level Three</dt>
  <dd class="col-sm-9">We work towards doing a version of the Harold, with more focus on group scenes, third beats and stronger comedic games. Must have taken Better Games Level Two, or had an exception based on outside expereince.</dd>
</dl>

<h2>Brandon Gardner Multi-Week Classes</h2>

<p>These courses are generally five two-hour sessions plus a show. 8 students, $220 each.</p>

<dl class="row">
  <dt class="col-sm-3">Getting Out Of Your Head About Game</dt>
  <dd class="col-sm-9">The Game of the Scene should come naturally out of improvisers listening, making choices and committing hard to their realities.  By focusing on these fundamentals students will come away from this class with habits that make finding and playing a game feel less effortful and more spontaneous.</dd>

  <dt class="col-sm-3">Let's Focus on Acting</dt>
  <dd class="col-sm-9">
    All of my favorite improvisers are also great actors.  They commit HARD to the reality of their scenes, they listen and react to their scene partners, and they allow their characters to be vulnerable and emotionally affected.  Their dedication to the acting side of improv makes the Games of their scenes that much stronger and funnier.  This class will focus on that acting side of improv.
  </dd>

  <dt class="col-sm-3">The Improvised Play</dt>
  <dd class="col-sm-9">This class will work towards creating fully improvised 30 minute plays.  Rather than focusing on the tropes of specific genres we will focus on acting and basic dramatic structure.  As in a monoscene the comedy will come from clear character Games but these multi-scene plays will also be built around the characters’ objectives, obstacles and actions.  Although the plays will almost always be funny we’ll make emotional honesty and commitment the priority.  The class will culminate with us improvising two mind-blowing one-act plays live on YouTube.  Some outside of class preparation will be required. </dd>
</dl>

<h2>Kat Palardy Classes and Workshops</h2>

<p>The clinic is two two-hour sessions plus a show. 8 people, $100 each.<br>
	The workshop is one three-hour session. 8 people, $50 each</p>

<dl class="row">
  <dt class="col-sm-3">The Drive-Thru Character Clinic</dt>
  <dd class="col-sm-9">Two weeks of two hour classes, plus a character monologue/scene screening.  Show up with an idea for a character or two, we'll find the fun, expand the world, and heighten your idea. You'll have some homework. Then in class two, we'll work out your character monologue or short scene. Then you'll have a week or two to shoot your monologue or scene (on your phone is fine!) and we'll do a screening.  </dd>
  
  <dt class="col-sm-3">Character Gym: Pitch and Play</dt>
  <dd class="col-sm-9">In this 3 hour workshop, you will pitch an idea for a character or two, then we'll work together to find the fun, expand and heighten. After that you’ll do some writing exercises to get specific and write a monologue or scene. Finally, we’ll share our work with each other! 
</dd>
  
</dl>


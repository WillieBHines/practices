<h2>Core Classes</h2>
<p>Each one of these is five two-hour sessions, plus a show (or sometimes two). 8 students per class, $220 each.</p>

<p>At WGIS,  "levels" are suggestions. This is a small community of students who mostly have studied improv already. So be your own judge. If you're itching to learn about The Harold, jump in there. Conversely, if you're sick of the Harold, try level 2 (monoscene). THAT SAID: If you've NEVER really done "game of the scene" style improv, I suggest level one first. Don't let "level one" fool you -- it's not a beginner's class. It's just the one that the other three are built on.</p>

<p>Also I've updated the names and descriptions of these levels but they are largely the same as what they've been the last 6 months, just named more specifically.</p>

<dl class="row">
  <dt class="col-sm-3">Level One: Monologue Show</dt>
  <dd class="col-sm-9">This is an intro to  “game of the scene” work. It's meant for new people and vets who want to strengthen game of the scene. We go over looking for the "unusual thing" and also being a good "voice of reason." We also practice pulling premises from a monologue.  Class show: monologues and scenes.</dd>

  <dt class="col-sm-3">Leve Two: Monoscene / Pretty Flower</dt>
  <dd class="col-sm-9">
Depending on the teacher, it's either Monoscene or Pretty Flower (those are forms). Either way the focus here is "point of view." What is YOUR character's deal? Class show: A Monoscene or Pretty Flower. 
  </dd>

  <dt class="col-sm-3">Level Three: Harold</dt>
  <dd class="col-sm-9">Meant for students ready for a challenge. Second beats, third beats, group games and connections. Use all of your improv muscles: an organic start, big heightening moves, patterns, being inspired by reality and tying it all up. Class show: Harold. </dd>
  
  <dt class="col-sm-3">Level Four: Teacher's Choice'</dt>
  <dd class="col-sm-9">Advanced class, the teacher picks the form.  Should have taken at least one of the other levels OR have 3+ years long-form experience somewhere. Email Will if you're not sure. Class show: It depends.</dd>
</dl>



